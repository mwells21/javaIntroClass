public class Player
{
  private int money;
  private int wager;


  public Player(int money, int wager)
  {
    this.money = money;
    this.wager = wager;

  }

  public int getMoney()
  {
    return money;
  }

  public int getWager()
  {
    return wager;
  }

  public setMoney(int money)
  {
    this.money = money;
  }

  public setWager(int wager)
  {
    this.wager = wager;

  }




}
